# Pehle ke tariko (On-Premises Deployment)


# 1. Hardware aur Software ki khareed: Company ko apne liye hardware aur software khareedna padta tha.
# 2. Setup aur Configuration: IT team ko hardware aur software setup aur configure karna padta tha.
# 3. Maintenance aur Updates: IT team ko hardware aur software ka maintenance aur updates karna padta tha.


# Samasyaein (Problems)


# 1. Lagat (Cost): Hardware aur software ki khareed mein lagat bahut adhik hoti thi.
# 2. Scalability: Company ke growth ke saath hardware aur software ko upgrade karna mushkil hota tha.
# 3. Security: Data security aur backup ka problem rehta tha.
# 4. Downtime: Hardware aur software failure ke kaaran downtime hota tha.


# Vyaapar par prabhav (Impact on Business)


# 1. Kam productivity: IT team ka samay hardware aur software maintenance mein lagta tha.
# 2. Kam scalability: Company ke growth ko rokta tha.
# 3. Kam security: Data security ka risk rehta tha.


# Cloud ke faayde (Benefits of Cloud)


# 1. Kam lagat (Reduced Cost): Pay-as-you-go model mein lagat kam hoti hai.
# 2. Scalability: Cloud providers scalability ka suvidha pradan karte hain.
# 3. Security: Cloud providers security aur backup ka suvidha pradan karte hain.
# 4. Uptime: Cloud providers uptime ka suvidha pradan karte hain.


# Cloud deployment models


# 1. Public Cloud: AWS, Azure, Google Cloud
# 2. Private Cloud: Company ke liye dedicated cloud
# 3. Hybrid Cloud: Public aur private cloud ka combination


# Cloud service models


# 1. IaaS (Infrastructure as a Service)
# 2. PaaS (Platform as a Service)
# 3. SaaS (Software as a Service)


